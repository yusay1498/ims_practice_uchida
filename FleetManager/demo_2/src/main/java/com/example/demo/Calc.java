package com.example.demo;

public class Calc {
    public int add(int x, int y) {
        return x + y;
    }

    public int sub(int x, int y) {
        return x - y;
    }

    public int multi(int x, int y) {
        return x * y;
    }

    public double div(int x, int y) {
        return (double) x / y;
    }

}
